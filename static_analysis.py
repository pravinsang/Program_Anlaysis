"""
This module provides functions for mathematical operations.
"""
def divide(a: int, b: int) -> float:
    """
    Divide two numbers.

    Args:
        a (int): The numerator.
        b (int): The denominator.

    Returns:
        float: The result of the division.
    """
    return a / b

result = divide(10, 0)
